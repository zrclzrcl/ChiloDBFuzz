-- Boundary type castings (Pattern 2.1, 2.2, 2.3)
-- Explicit CAST with edge values
SELECT CAST(9223372036854775807 AS SIGNED);
SELECT CAST(-9223372036854775808 AS UNSIGNED);
SELECT CAST('99999999999999999999999999999999' AS DECIMAL(65,30));
SELECT CAST(1e308 AS INTEGER);
SELECT CAST('not_a_number' AS INTEGER);

-- Implicit type casting via UNION (Pattern 2.2)
SELECT 1 UNION SELECT 'string';
SELECT NULL UNION SELECT 9223372036854775807;
SELECT 1.0 UNION SELECT 9223372036854775807;

-- Cross-function argument passing (Pattern 2.3)
SELECT ABS(LENGTH(REPEAT('a', 2147483647)));
SELECT SUBSTR(CAST(9223372036854775807 AS TEXT), 1, 100);
