-- Aggregate functions with boundary values
-- String aggregates with extreme lengths
SELECT GROUP_CONCAT(REPEAT('a', 10000) SEPARATOR REPEAT(',', 1000));
SELECT GROUP_CONCAT(DISTINCT col ORDER BY col) FROM (SELECT REPEAT('x', 10000) as col);

-- Numeric aggregates with overflow
SELECT SUM(9223372036854775807) FROM (SELECT 1 UNION ALL SELECT 2);
SELECT AVG(9223372036854775807);
SELECT COUNT(*) * 9223372036854775807;

-- Window functions with extreme frames
SELECT SUM(x) OVER (ORDER BY y ROWS BETWEEN 9223372036854775807 PRECEDING AND 9223372036854775807 FOLLOWING) FROM t;
SELECT LAG(x, 9223372036854775807) OVER (ORDER BY y) FROM t;
SELECT NTH_VALUE(x, 9223372036854775807) OVER (ORDER BY y) FROM t;
